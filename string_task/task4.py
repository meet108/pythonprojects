s = "this is string example"
print(s)

print(s[::-1])

a = s.split(" ")

print(a[0][::-1], a[1][::-1], a[2][::-1], a[3][::-1])

print(a[0][0:2][::-1] + a[0][2:4][::-1],
      a[1][0:2][::-1],
      a[2][0:2][::-1] + a[2][2:4][::-1] + a[2][4:6][::-1],
      a[3][0:2][::-1] + a[3][2:4][::-1] + a[3][4:6][::-1] + a[3][6]
      )

print("*".join(a))

b = s.replace(" is", " was")
print(b)
